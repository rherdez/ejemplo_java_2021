    private void limpiar(){
        this.txtNom.setText("");
        this.jSpinner1.setValue(0);
    }
    private void presentar(int x){
        this.txtNom.setText(p.get(x).getNombre());
        this.jSpinner1.setValue(p.get(x).getEdad());
        
    }
 
   ArrayList<persona> p=new ArrayList();
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        persona temp=new persona();
        temp.setNombre(this.txtNom.getText());
        temp.setEdad(Integer.valueOf(this.jSpinner1.getValue().toString()));
        
        p.add(temp);
        limpiar();
        this.txtpos.setText(String.valueOf(p.size()-1));
    }//GEN-LAST:event_jButton1ActionPerformed

